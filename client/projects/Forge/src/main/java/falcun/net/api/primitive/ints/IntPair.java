package falcun.net.api.primitive.ints;

import falcun.xyz.dev.boredhuman.dancore.falcunfork.util.Pair;

public class IntPair extends Pair<Integer, Integer> {
	public IntPair(Integer first, Integer second) {
		super(first, second);
	}
	public IntPair(int first, Integer second) {
		super(first, second);
	}
	public IntPair(Integer first, int second) {
		super(first, second);
	}
	public IntPair(int first, int second) {
		super(first, second);
	}
}
